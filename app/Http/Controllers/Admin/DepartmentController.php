    {
        $name= $request->input('name');
        $status= $request->input('status');




        $department =new Department();
        $department->name = $name;
        $department->status = $status;
        $department->save();
        // dd($request);

        return redirect('admin/department')->with('massage', 'Department Add Successfully');
    }

